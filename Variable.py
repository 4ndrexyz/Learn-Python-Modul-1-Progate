# - Mendefinisikan Variabel

name = 'Jhon'     # (name) nama variable dan ('Jhon') adalah nilai
number = 24       # (=) tetapkan sebuah nilai

# Untuk menyimpan nilai dalam variable, Anda perlu mendefiniskan variable.
# Anda dapat melakukannya dengan format berikut: variable_name = nilai. Operator = dalam Python bukan berarti "sama dengan".
# Operator tersebut digunakan untuk menetapkan nilai ke variable yang berada di sebelah kiri. Nama variable tidak perlu ditutup dengan tanda kutip.

