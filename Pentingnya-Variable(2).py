# Kegunaan lain dari variable adalah sebagai berikut:
# ・Anda dapat menggunakan data yang sama berulang kali.
# ・Jika Anda ingin mengganti nilai dari variable, Anda hanya perlu menggantinya sekali.


# code dengan variable

length = 5
area = length * length       # (lenght) Dengan menggunakan data yang sama berulang kali
print(area) 
# output = 25


# code tanpa variable

area = 5 * 5                 # (5 * 5) Butuh untuk menggantikan nilai berulang kali jika mau diubah
print(area)
# output = 25
